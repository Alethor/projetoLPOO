package tapetes;

/**
 *
 * @author erick
 */
public class Forma {
    
    protected double area;
    protected String tipo;

    public Forma() {
    }

    public Forma(double area) {
        this.area = area;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
    
    
}
